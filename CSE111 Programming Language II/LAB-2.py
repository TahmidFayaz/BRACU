def BMI(height, weight):
  out= weight/((height/100)**2)
  condition= f"Score is {out:.1f}. "
  if out < 18.5:
    condition+= "You are Underweight"
  if 18.5 <= out <= 24.9:
    condition += "you are Normal"
  if 25 <= out <= 30:
    condition += "you are Overweight"
  if out > 30:
    condition += "you are Obese"
  return condition
BMI(int(input()), int(input()))

