def capcheck(input1):
  out = ""
  idx = 0
  for i in input1:
    if idx == len(input1)-1:
      out += i
      break
    if idx == 0 and ord(i) in range(97,123):
      out += chr(ord(i)-32)
    elif input1[idx-2] in ".?!" and idx-2 > 0 and ord(i) in range(97,123):
      out += chr(ord(i)-32)
    elif input1[idx-1] == input1[idx+1] == " " and i == "i":
      out += "I"
    else:
      out += i
    idx += 1
  print(out)

