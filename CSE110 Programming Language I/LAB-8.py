def selectionasc(arr):
  for i in range(0, len(arr), 1):
    min = arr[i]
    minIndx = i
    for j in range(i, len(arr), 1):
      if(arr[j]<min):
        min = arr[j]
        minIndx = j
    arr[i], arr[minIndx] = arr[minIndx], arr[i]
  return arr
my_list= [10, 1, 20, 3, 6, 2, 5, 11, 15, 2, 12, 14, 17, 18, 29]
selectionasc(my_list)

