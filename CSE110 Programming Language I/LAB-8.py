def bubblelist(arr):
  for i in range(0, len(arr), 1):
    for j in range(0, len(arr)-1, 1):
      if(arr[j+1]<arr[j]):
        arr[j+1], arr[j]= arr[j], arr[j+1]
  return arr
my_list = [10, 1, 20, 3, 6, 2, 5, 11, 15, 2, 12, 14, 17, 18, 29]
bubblelist(my_list)

